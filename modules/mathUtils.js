export function getSum(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}
export function beDifferent(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}
export function getDivision(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}
export function getProduct(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}
